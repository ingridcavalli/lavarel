    <p>No topo da sociedade encontrava-se o Faraó e sua imensidão de parentes. O faraó era venerado como um verdadeiro deus, pois era considerado como o intermediário entre os 
        seres humanos e as demais divindades. Por isso, era uma monarquia teocrática, ou seja, um governo baseado nas ideias religiosas.
Abaixo do faraó e de sua família vinham as camadas privilegiadas como sacerdotes, nobres e funcionários. Na base da pirâmide social egípcia estavam os não privilegiados 
que eram artesãos, camponeses, escravos e soldados.
Os sacerdotes formavam, junto com os nobres, a corte real. 
Tanto a nobreza como o sacerdócio eram hereditários compondo a elite militar e latifundiária.
Os escribas estavam a serviço do Estado para planejar, fiscalizar e controlar a economia.
 Por isso, sabiam ler e escrever e eram eles que anotavam os feitos do faraó durante o seu reinado. Estes textos seriam colocados nos seus túmulos quando morressem.
Já o exército era constituído por jovens que eram convocados em tempo de guerra e soldados mercenários estrangeiros contratados pelo Estado.
Por sua parte, os artesãos eram trabalhadores assalariados que exerciam diferentes ofícios como cortadores de pedra, 
carpinteiros, joalheiros, etc. Os camponeses formavam a maior parte da população, trabalhavam na agricultura, na criação de animais e deviam pagar altos impostos.
Na sociedade egípcia, as mulheres tinham uma posição de prestígio. Podiam exercer 
qualquer função política, econômica ou social em igualdade com os homens de sua categoria social. Isto significava, inclusive, que poderiam ser faraós, 
como foi o caso de Cleópatra.</p>
